			<p>7 Cara Mudah dan Ampuh Mencegah Radiasi HP atau smartphone yang Berbahaya &ndash; Radiasi ponsel adalah radiasi yang disebabkan oleh ponsel anda, Radiasi ponsel juga memiliki dampak yang buruk untuk anda dan kesehatan anda, untuk itu akan saya berikan tips-tips bagaimana cara mengatasinya.</p>

			<h3><strong>Berikut tips bagaimana mengatasi Radiasi yang disebabkan oleh ponsel:</strong></h3>

			<h4><strong>1. Jangan menggunakan ponsel pada saat sinyal sedang dalam kondisi yang lemah (rendah).</strong></h4>

			<p>Hal ini dapat menyebabkan ponsel yang anda gunakan menjadi bekerja lebih keras dibandingkan anda meggunakanya pada saat berada dalam kondisi sinyal yang full, ponsel bekerja lebih keras untuk memperoleh koneksi yang maksimal, sehingga dapat menciptakan adanya kemungkinan radiasi yang lebih besar.</p>

			<h4><strong>2. Jangan menggunakan ponsel pada saat anda berada di ruang yang tertutup.</strong></h4>

			<p>Karena ponsel berusaha untuk mencari sinyal, maka dari kegiatanmencari sinyal tersebut terciptanya radiasi, jika anda berada di ruang tertutup maka radiasi akan semakin menyerang anda, dan tidak bisa menyebar di tempat lainya.</p>

			<h4><strong>3. Jangan menelpon pada saat anda sedang berkendara.</strong></h4>

			<p>Menelpon seseorang menggunakan ponsel anda sambil bergerak dengan cepat akan membuat ponsel Anda terus bekerja dan berusaha untuk menciptakan sambungan atau koneksi secara berulang-ulang ke menara (tower sinyal) berikutnya, sehingga akan melepaskan energi yang bisa menciandaptakan radiasi dalam jumlah besar.</p>

			<h4><strong>4. Membatasi penggunaan ponsel.</strong></h4>

			<p>Nah ini yang musti anda lakukan, bicara hanya seperlunya saja dan usahakan anda hanya mengkirimkan text (SMS, BBM, Chat) daripada menelpon.</p>

			<h4>5. Jangan mendekatkan ponsel dalam keadaan menyala di dekat tubuh anda.</h4>

			<p>Tubuh kita bisa dengan mudah menyerap berbagai radiasi yang bisa membahayakan tubuh, maka dari itu anda usahakan jauhkan ponsel anda dari anggota tubuh anda (termasuk juga di saku). Terkadang karena saking besarnya dan juga saking banyaknya kebutuhan kita dalam berkomunikasi, hingga membuat kita tidur di samping ponsel kita sendiri. Hal inilah yang sebaiknya anda hindari, karena dapat mengurangi kemungkinan besar terkenanya radiasi.</p>

			<h4>6. Pergunakan speakerphone.</h4>

			<p>Jika anda menginginkan diri sendiri serta juga anda tidak ingin mengganggu kenyamanan orang lain, maka sebaiknya anda menggunakan speakerphone, karena apabila anda menggunakan speakerphone, hal ini merupakan cara terbaik agar terhindar dari radiasi ponsel, karena memiliki jarak yang cukup aman dan jauh dari kepala Anda.</p>

			<h4>7. Jika anda menggunakan fitur bluetooth, maka alihkan dari satu sisi ke sisi yang lainnya.</h4>

			<p>Cara ini dapat kita lakukan untuk menghindari kita terkena radiasi yang terkonsentrasi pada satu sisi. Radiasi yang disebabkan oleh Bluetooth tetaplah berisiko walaupun lebih rendah dibandingkan radiasi pada ponsel.</p>

			<p>Nah itulah tips dan trik yang dapat saya sampaikan hari ini, semoga bermanfaat untuk anda semua, sekian dan saya ucapkan banyak terima kasih.</p>
