			<p><strong>Pengertian Routing</strong></p>

			<p>Routing merupakan suatu proses yang berguna untuk meneruskan paket jaringan dari satu jaringan ke jaringan yang lain.</p>

			<p>Routing juga bisa disebut sebagai sebuah metode penggabungan beberapa jaringan sehingga dapat mengalirkan paket data dari satu jaringan ke jaringan lain. Untuk melakukan hal tersebut, maka dibutuhkan suatu perangkat yang bernama router.</p>

			<p>Router tersebut akan menerima paket yang ditujukan ke jaringan luar, dan akan meneruskan paket tersebut dan akan diterima oleh router lainnya hingga sampai ke tujuannya.</p>

			<p><strong>Internetworking</strong></p>

			<p>Merupakan teknik cara menghubungkan jaringan satu ke jaringan yang lain menggunakan gateway.</p>

			<p><strong>Proses Routing</strong></p>

			<p><strong>1. Static Routing</strong></p>

			<p>Merupakan salah satu tipe routing yang memiliki tabel routing statik dan di setting secara manual oleh administrator jaringan dan juga merupakan pengaturan routing yang paling sederhana dan dapat dilakukan pada jaringan komputer.</p>

			<p><strong>2. Dynamic Routing</strong></p>

			<p>Merupakan salah satu cara routing dimana router yang digunakan dapat membaca atau mempelajari sendiri route terbaik yang akan ditempuh router tersebut untuk meneruskan paket dari sebuah network ke network lainnya.</p>

			<p>Administrator jaringan tidak perlu menentukan rute yang harus ditempuh oleh paket-paket tersebut.</p>

			<p><strong>Macam-Macam Dynamic Routing</strong></p>

			<ol>
				<li><strong>RIP</strong><br />
					Merupakan sebuah protokol routing dinamis yang digunakan didalam jaringan LAN dan WAN</li>
				<li><strong>OSPF</strong><br />
					Merupakan protokol gateway interior (IGP) untuk routing Internet Protocol (IP) paket hanya dalam satu domain routing.</li>
				<li><strong>BGP</strong><br />
					Merupakan gateway yang dirancang untuk bertukar informasi routing.</li>
				<li><strong>IGRP</strong><br />
					Merupakan istance vector interior gateway protokol (IGP) yang dikembangkan oleh cisco digunakan untuk bertukar data routing dalam suatu autonomous system.</li>
			</ol>

